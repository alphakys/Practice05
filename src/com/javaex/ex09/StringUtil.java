public class StringUtil {
    
    public static String concatString(String str[]){
    		
    	String result="";
    	
    	for(int i=0; i<str.length;i++) {
    		
    		result+=str[i];
    		
    	}return result;
    	
    	   	
    	
    	
    	
    	
    	
        //메소드 내용작성
        
    }

}
